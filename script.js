// انیمیشن تغییر نقش‌های شغلی
const roles = ["IT Systems Administrator", "Cybersecurity Specialist", "Cloud Enthusiast", "DevOps Learner"];
let index = 0;
let dynamicTextElement = document.querySelector(".dynamic-text");

function updateDynamicText() {
    dynamicTextElement.innerText = roles[index];
    index = (index + 1) % roles.length;
}

setInterval(updateDynamicText, 3000);
updateDynamicText();
